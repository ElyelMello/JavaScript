let sinal = "/"
let a = 3;
let b = 2;
switch(sinal){
    case "+":
        console.log(`O resultado da soma:  ${a} + ${b} é ${a + b}.`);
        break;
    
    case "-":
        console.log(`O resultado da subtração: ${a} - ${b} é ${a - b}.`);
        break;
    
    case "*":
        console.log(`O resultado da multiplicação: ${a} * ${b} é ${a * b}.`);
        break;

    case "/":
        console.log(`O resultado da divisão: ${a} / ${b} é ${a / b}.`);
        break;
}